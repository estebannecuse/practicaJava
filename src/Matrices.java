public class Matrices {
    public static void main(String[] args) {
        //defino como constante 
        final int Renglones = 2;
        final int Columnas = 3;
        //creo un array bidimensional
        int[][] matriz = new int[Renglones][Columnas];
        // lleno la matriz
        matriz[0][0] = 100;
        matriz[0][1] = 200;
        matriz[0][2] = 300;
        matriz[1][0] = 400;
        matriz[1][1] = 500;
        matriz[1][2] = 600;
    }
}
